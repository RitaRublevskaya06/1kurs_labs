void Object::deleteList()
{
	while (Head != nullptr)
	{
		Element* temp = Head;
		Head = Head->Next;
		delete temp;
	}
}

int Object::countList()
{
	int count = 0;
	Element* current = Head;
	while (current != nullptr)
	{
		count++;
		current = current->Next;
	}
	return count;
}

