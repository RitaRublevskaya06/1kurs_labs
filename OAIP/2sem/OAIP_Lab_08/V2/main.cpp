#include "header.h"

int main() {
    setlocale(LC_ALL, "Rus");
    Node* front = nullptr;
    Node* rear = nullptr;
    int size, choice, value;

    std::cout << "Введите размер очереди: ";
    std::cin >> size;

    std::cout << "1 - Ввод элементов очереди" << std::endl;
    std::cout << "2 - Вывод очереди" << std::endl;
    std::cout << "3 - Найти максимальный элемент" << std::endl;
    std::cout << "4 - Найти минимальный элемент" << std::endl;
    std::cout << "5 - Найти количество элементов между минимальным и максимальным" << std::endl;
    std::cout << "6 - Очистка очереди" << std::endl;
    std::cout << "7 - Выход" << std::endl;

    for (;;) {
        std::cout << "Выберите команду: ";
        std::cin >> choice;

        switch (choice) {
            case 1:
                std::cout << "Введите элемент для добавления в очередь: ";
                std::cin >> value;
                enqueue(&front, &rear, value);
                break;

            case 2:
                std::cout << "Очередь: ";
                display(front);
                break;

            case 3:
                std::cout << "Максимальный элемент в очереди: " << findMax(front) << std::endl;
                break;

            case 4:
                std::cout << "Минимальный элемент в очереди: " << findMin(front) << std::endl;
                break;

            case 5:
                int minVal, maxVal;
                std::cout << "Введите минимальное значение: ";
                std::cin >> minVal;
                std::cout << "Введите максимальное значение: ";
                std::cin >> maxVal;
                std::cout << "Количество элементов между минимальным и максимальным: "
                          << countElementsBetween(front, minVal, maxVal) << std::endl;
                break;

            case 6:
                clearQueue(&front);
                std::cout << "Очередь очищена." << std::endl;
                break;

            case 7:
                clearQueue(&front);
                return 0;

            default:
                std::cout << "Неверная команда. Попробуйте снова." << std::endl;
                break;
        }
    }

    return 0;
}